$("h1").before("<button>New</button>"); // Adicionando um botão antes do h1

$("h1").after("<button>New</button>"); // Adicionando um botão depois do h1

$("h1").prepend("<button>New</button>"); // Adicionando um botão logo após a tag de abertura

$("h1").append("<button>New</button>"); // Adicionando um botão logo antes da tag de fechamento

$("button").remove(); // Removendo todos os botões